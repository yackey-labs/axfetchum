#[test]
fn check_detects_out_of_sync() {
    use std::io::Write;

    let routes = sample_routes();
    let dir = std::env::temp_dir().join("axfetchum_test");
    std::fs::create_dir_all(&dir).unwrap();
    let output_path = dir.join("generated.ts");

    let config = GeneratorConfig {
        output_path: output_path.to_string_lossy().into(),
        ..yauth_config()
    };

    // Write wrong content
    let mut f = std::fs::File::create(&output_path).unwrap();
    writeln!(f, "// old content").unwrap();
    drop(f);

    // Check should fail
    let result = axfetchum::check(&routes, &config);
    assert!(result.is_err());

    // Now write correct content
    axfetchum::generate_to_file(&routes, &config).unwrap();

    // Check should pass
    let result = axfetchum::check(&routes, &config);
    assert!(result.is_ok());

    // Cleanup
    std::fs::remove_dir_all(&dir).ok();
}

