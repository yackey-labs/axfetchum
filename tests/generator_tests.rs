use axfetchum::{GeneratorConfig, api_routes, generate};

fn yauth_config() -> GeneratorConfig {
    GeneratorConfig {
        bindings_dir: "./bindings".into(),
        output_path: "./packages/client/src/generated.ts".into(),
        factory_name: "createYAuthClient".into(),
        enable_groups: true,
        error_class_name: "YAuthError".into(),
        options_interface_name: "YAuthClientOptions".into(),
        default_credentials: "include".into(),
        type_import_prefix: "../../../../bindings".into(),
        format_command: None,
    }
}

fn sample_routes() -> axfetchum::RouteCollection {
    let mut routes = api_routes! {
        getSession: GET "/session" [auth]
            -> SessionResponse;
        logout: POST "/logout" [auth]
            -> SuccessResponse;
        updateProfile: PATCH "/me" [auth]
            body: UpdateProfileRequest -> ProfileResponse;
    };

    let ep = api_routes! {
        @group emailPassword

        register: POST "/register"
            body: RegisterRequest -> MessageResponse;
        login: POST "/login"
            body: LoginRequest -> LoginResponse;
        verify: POST "/verify-email"
            body: VerifyEmailRequest -> MessageResponse;
        changePassword: POST "/change-password" [auth]
            body: ChangePasswordRequest -> MessageResponse;
    };
    routes.extend(ep);

    let admin = api_routes! {
        @group admin

        listUsers: GET "/admin/users" [auth]
            query: ListUsersQuery -> ListUsersResponse;
        getUser: GET "/admin/users/{id}" [auth]
            -> UserResponse;
        deleteUser: DELETE "/admin/users/{id}" [auth];
        banUser: POST "/admin/users/{id}/ban" [auth]
            body: BanRequest -> UserResponse;
    };
    routes.extend(admin);

    let oauth = api_routes! {
        @group oauth

        authorize: GET "/oauth/{provider}/authorize" [redirect]
            query: AuthorizeQuery;
        callback: POST "/oauth/{provider}/callback"
            body: CallbackBody -> AuthResponse;
    };
    routes.extend(oauth);

    routes
}

#[test]
fn generates_valid_output() {
    let routes = sample_routes();
    let config = yauth_config();
    let output = generate(&routes, &config);

    // Header
    assert!(output.starts_with("// Auto-generated by axfetchum. Do not edit.\n"));

    // Imports exist for custom types
    assert!(output.contains("import type { RegisterRequest }"));
    assert!(output.contains("import type { LoginResponse }"));
    assert!(output.contains("import type { ListUsersQuery }"));
    assert!(output.contains("import type { UserResponse }"));
    assert!(output.contains("import type { AuthorizeQuery }"));

    // Error class
    assert!(output.contains("export class YAuthError extends Error"));

    // Options interface
    assert!(output.contains("export interface YAuthClientOptions"));

    // Factory function
    assert!(output.contains("export function createYAuthClient(options: YAuthClientOptions)"));

    // Type export
    assert!(output.contains("export type YAuthClient = ReturnType<typeof createYAuthClient>;"));
}

